# 標準 L bucket 值
L_BUCKETS = [500, 1000, 1500, 2000, 2500]


def get_type65_data(line_size: str) -> dict | None:
    """依管徑查 Type 65 資料"""
    return TYPE65_TABLE.get(line_size)


def snap_l_bucket(l_mm: int) -> int | None:
    """將 L 值向上取至最近 bucket，超出 2500 回傳 None"""
    for b in L_BUCKETS:
        if l_mm <= b:
            return b
    return None


